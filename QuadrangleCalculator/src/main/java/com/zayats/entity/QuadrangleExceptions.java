package com.zayats.entity;

public class QuadrangleExceptions extends Exception {

    public QuadrangleExceptions() {
    }

    public QuadrangleExceptions(String message) {
        super(message);
    }

    public QuadrangleExceptions(String message, Throwable cause) {
        super(message, cause);
    }

    public QuadrangleExceptions(Throwable cause) {
        super(cause);
    }
}